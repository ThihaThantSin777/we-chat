  String phNo;

  CountryCodeVO(this.name, this.phNo);

  @override
  String toString() {
    return 'CountryCodeVO{name: $name, phNo: $phNo}';
  }

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is CountryCodeVO &&
          runtimeType == other.runtimeType &&
          name == other.name &&
          phNo == other.phNo;

  @override
  int get hashCode => name.hashCode ^ phNo.hashCode;

  @override
  String getSuspensionTag() =>name[0].toUpperCase();
}

